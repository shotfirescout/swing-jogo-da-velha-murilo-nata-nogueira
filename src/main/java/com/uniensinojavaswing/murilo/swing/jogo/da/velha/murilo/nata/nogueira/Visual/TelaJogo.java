    private void esvaziaMatriz(){
        for(int i = 0; i < 3; i++)
        {
            for(int j = 0; j < 3; j++) {
                tabuleiro[i][j] = "";
            }
        }
    }
    
    private boolean isFull() {
        for(int i = 0; i < 3; i++)
        {
            for(int j = 0; j < 3; j++) {
                if(tabuleiro[i][j].equals(""))
                {
                    return false;
                }
            }
        }
        return true;
    }

    //Ação geral para todos os botões que estão dentro do campo para o jogo da velha
    private void btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActionPerformed
        JButton botao = (JButton) evt.getSource();
        final int linha = (int) botao.getClientProperty("linha");
        final int coluna = (int) botao.getClientProperty("coluna");
        if(botao.getText().isEmpty())
        {
            String suporte = isPlayer1 ? "X" : "O"; //Define o suporte que vai ajudar na pesquisa
            botao.setText(isPlayer1 ? "X" : "O"); //Coloca o texto referente ao jogador
            tabuleiro[linha][coluna] = isPlayer1 ? "X" : "O";
            jogadas++;
            fimJogo = controleJogo.verificaGanhador(tabuleiro, suporte);
            if(fimJogo)
            {
                logicaFinalJogo(false);
            } else if(isFull())
            {
                logicaFinalJogo(true);
            }
            
            
            isPlayer1 = !isPlayer1;
            textIconeAtual.setText(isPlayer1 ? "X" : "O");
            
        }
        
        //Desliga o botão
        botao.setEnabled(false);
    }//GEN-LAST:event_btnActionPerformed

    private void BtnReiniciarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnReiniciarActionPerformed
        reativarBotoes();
        esvaziaMatriz();
        isPlayer1 = true;
        textIconeAtual.setText("X");
    }//GEN-LAST:event_BtnReiniciarActionPerformed

    private void logicaFinalJogo(boolean isEmpate){
        if(isEmpate)
        {
            JOptionPane.showMessageDialog(this, "Empate!");
            empates++;
        } else {
            JOptionPane.showMessageDialog(this, isPlayer1 ? "Jogador1 Ganhou" : "Jogador 2 Ganhou!");
            if(isPlayer1)
            {
                pontosX++;
                textParabens1.setVisible(true);
            } else {
                pontosO++;
                textParabens2.setVisible(true);
            }
        }
        
        desligarTodosBotoes();
        atualizarPontos();
    }
    
